CODE_BLOCK_RE = re.compile(r"(```[\s\S]*?```)")


def apply_replacements(text: str) -> str:
    """전체 텍스트에 변환 규칙을 적용한다. 코드 블록은 건드리지 않는다."""
    # 코드 블록 분할: 짝수 인덱스는 일반 텍스트, 홀수 인덱스는 코드 블록
    parts = CODE_BLOCK_RE.split(text)

    for i in range(len(parts)):
        if i % 2 == 1:
            continue  # 코드 블록은 건드리지 않음
        # 일반 치환
        for pattern, replacement in REPLACEMENTS:
            parts[i] = re.sub(pattern, replacement, parts[i])
        # ~다 종결 치환 (다중 flag 가능성 처리)
        for rule in VERB_ENDING_RULES:
            if len(rule) == 2:
                pat, repl = rule
                parts[i] = re.sub(pat, repl, parts[i])
            else:
                pat, repl, flags = rule
                parts[i] = re.sub(pat, repl, parts[i], flags=flags)

    return "".join(parts)


def collect_md_files(target: Path) -> list[Path]:
    if target.is_file():
        return [target] if target.suffix == ".md" else []
    if target.is_dir():
        out: list[Path] = []
        for p in sorted(target.rglob("*.md")):
            rel = p.relative_to(target) if target.is_absolute() else p
            parts = set(rel.parts)
            # conventions·assets·resources·figures는 변환 대상 아님 (집필 가이드·도식 명세)
            if {"conventions", "assets", "resources", "figures"} & parts:
                continue
            out.append(p)
        return out
    return []


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description="한국식 톤 자동 변환")
    parser.add_argument("path", help="대상 파일 또는 디렉토리")
    parser.add_argument("--dry-run", action="store_true",
                        help="변환 결과를 표시만 하고 파일에 쓰지 않음")
    args = parser.parse_args(argv[1:])

    target = Path(args.path).resolve()
    if not target.exists():
        print(f"path not found: {target}", file=sys.stderr)
        return 2

    files = collect_md_files(target)
    if not files:
        print(f"no .md files: {target}", file=sys.stderr)
        return 2

    modified_count = 0
    total_changes = 0
    for f in files:
        try:
            original = f.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError) as e:
            print(f"  skip (read error): {f}: {e}", file=sys.stderr)
            continue
        modified = apply_replacements(original)
        if modified != original:
            # 변경된 줄 수 카운트
            orig_lines = original.splitlines()
            mod_lines = modified.splitlines()
            diff = sum(1 for a, b in zip(orig_lines, mod_lines) if a != b)
            diff += abs(len(orig_lines) - len(mod_lines))
            total_changes += diff
            modified_count += 1
            rel = f.relative_to(Path.cwd()) if str(f).startswith(str(Path.cwd())) else f
            if args.dry_run:
                print(f"  would modify ({diff} lines): {rel}")
            else:
                f.write_text(modified, encoding="utf-8")
                print(f"  modified ({diff} lines): {rel}")

    print()
    print(f"파일 {len(files)}개 검사 / {modified_count}개 변경 / 약 {total_changes}줄 영향")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
